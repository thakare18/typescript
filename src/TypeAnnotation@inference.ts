// Type Annotation:
// When we manually specify the type of a variable or function.
// Syntax: variableName: type

let age: number = 21;
let name: string = "Prathamesh";


// Type Inference:
// TypeScript automatically guesses the type based on the value assigned.

let city = "Mumbai"; // TypeScript infers type as string
let marks = 90;      // TypeScript infers type as number